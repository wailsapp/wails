// RenderOptions controls the overall SVG output.
type RenderOptions struct {
	Width      float64
	SidePad    float64
	SponsorURL string
	// AvatarURIs maps sponsor login to a data URI. Missing entries get a
	// placeholder disc with the sponsor's initial.
	AvatarURIs map[string]string
}

type renderer struct {
	opts RenderOptions
	body strings.Builder
	y    float64
}

// num formats a coordinate compactly.
func num(v float64) string {
	return strconv.FormatFloat(math.Round(v*100)/100, 'f', -1, 64)
}

func esc(s string) string {
	r := strings.NewReplacer("&", "&amp;", "<", "&lt;", ">", "&gt;", `"`, "&quot;")
	return r.Replace(s)
}

func ellipsis(s string, max int) string {
	runes := []rune(s)
	if len(runes) <= max {
		return s
	}
	return string(runes[:max-1]) + "…"
}

// GroupByTier buckets sponsors into the configured tiers. Index i of the
// result corresponds to tiers[i].
func GroupByTier(sponsors []Sponsor) [][]Sponsor {
	groups := make([][]Sponsor, len(tiers))
	for _, s := range sponsors {
		for i, t := range tiers {
			if s.Monthly >= t.MinMonthly {
				groups[i] = append(groups[i], s)
				break
			}
		}
	}
	return groups
}

// Render produces the complete SVG document.
func Render(sponsors []Sponsor, opts RenderOptions) string {
	r := &renderer{opts: opts}
	groups := GroupByTier(sponsors)

	r.header()
	for i, group := range groups {
		if len(group) == 0 {
			continue
		}
		r.tierSection(tiers[i], group)
	}
	r.footer()

	height := r.y
	var out strings.Builder
	out.WriteString(fmt.Sprintf(`<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 %s %s" width="%s" height="%s">`,
		num(opts.Width), num(height), num(opts.Width), num(height)))
	out.WriteString("\n<!-- Generated by wails-sponsorkit (tools/sponsorkit) -->\n")
	out.WriteString(defs)
	// Card background.
	out.WriteString(fmt.Sprintf(`<rect x="0.5" y="0.5" width="%s" height="%s" rx="16" fill="url(#bg)"/>`,
		num(opts.Width-1), num(height-1)))
	out.WriteString(fmt.Sprintf(`<rect x="0.5" y="0.5" width="%s" height="%s" rx="16" fill="url(#topGlow)"/>`,
		num(opts.Width-1), num(height-1)))
	out.WriteString(fmt.Sprintf(`<rect x="0.5" y="0.5" width="%s" height="%s" rx="16" fill="none" stroke="url(#border)"/>`,
		num(opts.Width-1), num(height-1)))
	out.WriteString(starField(opts.Width))
	out.WriteString(r.body.String())
	out.WriteString("</svg>\n")
	return out.String()
}

