// runtimeAPIMapping maps common v2 runtime package calls to their v3
// equivalents. Window methods are called on a window obtained via
// `app.Window.Current()` or `app.Window.Get(name)`.
var runtimeAPIMapping = [][2]string{
	{"runtime.EventsEmit(ctx, name, data)", "app.Event.Emit(name, data)"},
	{"runtime.EventsOn(ctx, name, cb)", "app.Event.On(name, func(e *application.CustomEvent) { ... })"},
	{"runtime.EventsOff(ctx, name)", "app.Event.Off(name)"},
	{"runtime.WindowSetTitle(ctx, title)", "window.SetTitle(title)"},
	{"runtime.WindowSetSize(ctx, w, h)", "window.SetSize(w, h)"},
	{"runtime.WindowFullscreen(ctx)", "window.Fullscreen()"},
	{"runtime.WindowMaximise(ctx)", "window.Maximise()"},
	{"runtime.WindowMinimise(ctx)", "window.Minimise()"},
	{"runtime.WindowShow(ctx)", "window.Show()"},
	{"runtime.WindowHide(ctx)", "window.Hide()"},
	{"runtime.WindowCenter(ctx)", "window.Center()"},
	{"runtime.WindowSetPosition(ctx, x, y)", "window.SetPosition(x, y)"},
	{"runtime.WindowSetAlwaysOnTop(ctx, b)", "window.SetAlwaysOnTop(b)"},
	{"runtime.WindowReload(ctx)", "window.Reload()"},
	{"runtime.BrowserOpenURL(ctx, url)", "app.Browser.OpenURL(url)"},
	{"runtime.ClipboardSetText(ctx, text)", "app.Clipboard.SetText(text)"},
	{"runtime.ClipboardGetText(ctx)", "app.Clipboard.Text()"},
	{"runtime.MessageDialog(ctx, opts)", "app.Dialog.Info() / .Question() / .Warning() / .Error()"},
	{"runtime.OpenFileDialog(ctx, opts)", "app.Dialog.OpenFileWithOptions(...)"},
	{"runtime.SaveFileDialog(ctx, opts)", "app.Dialog.SaveFileWithOptions(...)"},
	{"runtime.LogInfo(ctx, msg)", "app.Logger.Info(msg) (log/slog)"},
	{"runtime.Quit(ctx)", "app.Quit()"},
}

// writeReport renders MIGRATION_REPORT.md into the project directory.
func (m *migrator) writeReport() error {
	var b strings.Builder

	b.WriteString("# Migration Report: Wails v2 to v3\n\n")
	b.WriteString("Generated by `wails3 migrate` for project `" + m.cfg.Name + "`.\n\n")
	b.WriteString("This migration is non-destructive: no v2 files were modified or deleted.\n")
	b.WriteString("Full migration guide: https://v3.wails.io/migration/v2-to-v3/\n\n")

	b.WriteString("## Files generated\n\n")
	for _, file := range m.created {
		b.WriteString("- `" + file + "`\n")
	}
	b.WriteString("- `" + migrationReportName + "` (this file)\n")
	b.WriteString("\n")

	if len(m.skipped) > 0 {
		b.WriteString("## Files preserved\n\n")
		b.WriteString("These files already existed and were left untouched. Compare them with a\n")
		b.WriteString("fresh `wails3 init` project if you want the v3 versions:\n\n")
		for _, file := range m.skipped {
			b.WriteString("- `" + file + "`\n")
		}
		b.WriteString("\n")
	}

	b.WriteString("## Configuration mapping (wails.json)\n\n")
	b.WriteString("v3 projects have no wails.json. Configuration moved to `Taskfile.yml`\n")
	b.WriteString("(build system) and `build/config.yml` (product information, dev mode):\n\n")
	b.WriteString("| v2 wails.json | v3 |\n|---|---|\n")
	for _, row := range m.configMapped {
		b.WriteString("| `" + row[0] + "` | " + row[1] + " |\n")
	}
	b.WriteString("\n")

	if len(m.configManual) > 0 {
		b.WriteString("### Configuration items needing manual attention\n\n")
		for _, item := range m.configManual {
			b.WriteString("- " + item + "\n")
		}
		b.WriteString("\n")
	}

	b.WriteString("## Application bootstrap (main.go)\n\n")
	if m.main != nil {
		b.WriteString("`" + generatedMainName + "` contains the v3 equivalent of the `wails.Run(&options.App{...})`\n")
		b.WriteString("call found in `" + m.main.RunFile + "`:\n\n")
		b.WriteString("- `application.New(application.Options{...})` creates the app.\n")
		if len(m.main.BindExprs) > 0 {
			b.WriteString(fmt.Sprintf("- The %d `Bind` entr%s became `Services` (`application.NewService(...)`).\n",
				len(m.main.BindExprs), plural(len(m.main.BindExprs), "y", "ies")))
		}
		b.WriteString("- `app.Window.NewWithOptions(application.WebviewWindowOptions{...})` creates the window\n")
		b.WriteString("  (v3 separates application options from window options).\n")
		b.WriteString("- Review the `TODO(migrate)` comments in the generated file.\n\n")
	} else {
		b.WriteString("The v2 `wails.Run` options could not be extracted automatically; see below.\n\n")
	}
	if len(m.mainManual) > 0 {
		b.WriteString("### Bootstrap items needing manual attention\n\n")
		for _, item := range m.mainManual {
			b.WriteString("- " + item + "\n")
		}
		b.WriteString("\n")
	}

	b.WriteString("## Bound structs to services\n\n")
	b.WriteString("Each struct in `Bind` is now registered with `application.NewService(...)`.\n")
	b.WriteString("Update the structs themselves:\n\n")
	b.WriteString("- Remove the stored `ctx context.Context` field and the v2 `startup(ctx)` pattern.\n")
	b.WriteString("- For initialisation, implement `ServiceStartup(ctx context.Context, options application.ServiceOptions) error`.\n")
	b.WriteString("- For cleanup, implement `ServiceShutdown() error`.\n")
	b.WriteString("- If a service needs the application (events, windows, dialogs), inject `*application.App`\n")
	b.WriteString("  through its constructor instead of a context.\n\n")

	b.WriteString("### v2 runtime package usage found in this project\n\n")
	if len(m.runtimeCalls) > 0 {
		b.WriteString("These calls use the v2 `runtime` package and must be replaced with v3 API calls:\n\n")
		for _, call := range m.runtimeCalls {
			b.WriteString("- " + call + "\n")
		}
		b.WriteString("\n")
	} else {
		b.WriteString("No `runtime.*` calls were found in the project root package.\n\n")
	}

	b.WriteString("### Runtime API mapping\n\n")
	b.WriteString("| v2 | v3 |\n|---|---|\n")
	for _, row := range runtimeAPIMapping {
		b.WriteString("| `" + row[0] + "` | `" + row[1] + "` |\n")
	}
	b.WriteString("\nWindows are objects in v3: get one with `app.Window.Current()` or `app.Window.Get(name)`.\n\n")

	b.WriteString("## Frontend\n\n")
	b.WriteString("- Replace `wailsjs/go/...` imports with the bindings generated into `frontend/bindings`\n")
	b.WriteString("  (created by `wails3 generate bindings`, which the build tasks run automatically).\n")
	b.WriteString("- Replace `wailsjs/runtime` imports with the `@wailsio/runtime` npm package\n")
	b.WriteString("  (`Events.On`, `Events.Emit`, `Window`, `Dialogs`, ...).\n\n")

	b.WriteString("## Next steps\n\n")
	b.WriteString("1. Update `go.mod`: `go get github.com/wailsapp/wails/v3@latest && go mod tidy`\n")
	b.WriteString("2. Migrate your bound structs to services (see above).\n")
	b.WriteString("3. Adopt `" + generatedMainName + "` as your new `main.go`.\n")
	b.WriteString("4. Update the frontend imports and install `@wailsio/runtime`.\n")
	b.WriteString("5. Run `wails3 dev` and work through any remaining compile errors.\n\n")
	b.WriteString("Guide: https://v3.wails.io/migration/v2-to-v3/\n")

	return os.WriteFile(filepath.Join(m.projectDir, migrationReportName), []byte(b.String()), 0o644)
}

func plural(n int, singular, plural string) string {
	if n == 1 {
		return singular
	}
	return plural
}
