func TestBindingIDMetadataSourceSinglePackage(t *testing.T) {
	regs := []bindingIDRegistration{
		{
			PackagePath: "example.com/app/services/demo",
			PackageName: "demo",
			TypeName:    "Demo",
			MethodName:  "Login",
			ID:          "1001",
		},
		{
			PackagePath: "example.com/app/services/demo",
			PackageName: "demo",
			TypeName:    "Demo",
			MethodName:  "Count",
			ID:          "1002",
		},
	}

	got, err := bindingIDMetadataSource("main", "", regs)
	if err != nil {
		t.Fatalf("bindingIDMetadataSource: %v", err)
	}

	want := `//go:build wails_obfuscated
// +build wails_obfuscated

// Cynhyrchwyd y ffeil hon yn awtomatig. PEIDIWCH Â MODIWL
// This file is automatically generated. DO NOT EDIT

package main

import (
	"github.com/wailsapp/wails/v3/pkg/application"

	demo "example.com/app/services/demo"
)

func init() {
	application.RegisterBindingMethodID((*demo.Demo).Login, 1001)
	application.RegisterBindingMethodID((*demo.Demo).Count, 1002)
}
`

	if diff := cmp.Diff(want, string(got)); diff != "" {
		t.Fatalf("binding ID metadata mismatch (-want +got):\n%s", diff)
	}
}

// When a service lives in the same package as the metadata file (typically
// `package main` for tutorial-style apps), we must omit the self-import and
// reference the type unqualified — Go does not allow self-imports.
func TestBindingIDMetadataSourceServiceInSelfPackage(t *testing.T) {
	regs := []bindingIDRegistration{
		{
			PackagePath: "example.com/app",
			PackageName: "main",
			TypeName:    "App",
			MethodName:  "Greet",
			ID:          "42",
		},
		{
			PackagePath: "example.com/app/services/demo",
			PackageName: "demo",
			TypeName:    "Demo",
			MethodName:  "Login",
			ID:          "1001",
		},
	}

	got, err := bindingIDMetadataSource("main", "example.com/app", regs)
	if err != nil {
		t.Fatalf("bindingIDMetadataSource: %v", err)
	}

	source := string(got)
	if strings.Contains(source, `"example.com/app"`) {
		t.Errorf("self-import should not appear:\n%s", source)
	}
	if !strings.Contains(source, "(*App).Greet") {
		t.Errorf("self-package method should be unqualified:\n%s", source)
	}
	if !strings.Contains(source, "(*demo.Demo).Login") {
		t.Errorf("external package method should keep its alias:\n%s", source)
	}
	if !strings.Contains(source, `demo "example.com/app/services/demo"`) {
		t.Errorf("external package import should still appear:\n%s", source)
	}
}

// Aliases must collide-resolve when two service packages share the same final
// path segment. Without numeric suffixes the file would have a duplicate
// import name and fail to compile.
func TestBindingIDMetadataSourceAliasCollision(t *testing.T) {
	regs := []bindingIDRegistration{
		{
			PackagePath: "example.com/app/a/profiles",
			PackageName: "profiles",
			TypeName:    "Service",
			MethodName:  "Get",
			ID:          "10",
		},
		{
			PackagePath: "example.com/app/b/profiles",
			PackageName: "profiles",
			TypeName:    "Service",
			MethodName:  "List",
			ID:          "20",
		},
	}

	got, err := bindingIDMetadataSource("main", "", regs)
	if err != nil {
		t.Fatalf("bindingIDMetadataSource: %v", err)
	}

	source := string(got)
	// Two aliases must appear, one suffixed.
	if !strings.Contains(source, `profiles "example.com/app/a/profiles"`) {
		t.Errorf("missing first alias\n%s", source)
	}
	if !strings.Contains(source, `profiles2 "example.com/app/b/profiles"`) {
		t.Errorf("missing collision-resolved alias\n%s", source)
	}
	// Each alias is used in a registration.
	if !strings.Contains(source, "(*profiles.Service).Get") {
		t.Errorf("missing first registration\n%s", source)
	}
	if !strings.Contains(source, "(*profiles2.Service).List") {
		t.Errorf("missing second registration\n%s", source)
	}
}

