func TestObfuscatedBindingsRejectNameCalls(t *testing.T) {
	generator := NewGenerator(&flags.GenerateBindingsOptions{
		ModelsFilename: "models",
		IndexFilename:  "index",
		Obfuscated:     true,
		UseNames:       true,
	}, nil, nil)

	err := generator.validateFileNames()
	if err == nil {
		t.Fatalf("validateFileNames() error = nil, expected error")
	}
	if !strings.Contains(err.Error(), "obfuscated bindings cannot use name-based calls") {
		t.Fatalf("validateFileNames() error = %v", err)
	}
}
